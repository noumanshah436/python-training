# *********************************************************
# Python 2D lists  or two dimensional list
# 2D lists = a list of lists
# *********************************************************

print("2D lists")

drinks = ["coffee", "soda", "tea"]
dinner = ["pizza","hamburger","hotdog"]
dessert = ["cake","ice cream"]

food = [drinks,dinner,dessert]

print(food)           # show complete 2D lists
print(food[0])        # print list at specific index of 2D lists
print(food[0][0])     # print specific element of  2D lists
print(food[0][0][2])  # print specific index of specific element of  2D lists

